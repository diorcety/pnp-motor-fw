/***************************************************************//**
   \fn LoadEeprom()
   \brief Load Data from EEPROM
********************************************************************/
static void LoadEeprom()
{
  UCHAR valid;
  {
    TModuleEeprom* eeprom = GetModuleEeprom(&valid);
    if (valid)
    {
      memcpy(&ModuleConfig, &(eeprom->config), sizeof(TModuleConfig));  //Take data from EEPROM
    }
    else
    {
      memcpy(&(eeprom->config), &ModuleConfig, sizeof(TModuleConfig));  //Put default data to EEPROM
    }
  }

  for (int i = 0; i < N_O_MOTORS; ++i)
  {
    TMotorEeprom* eeprom = GetMotorEeprom(i, &valid);
    if (valid)
    {
      memcpy(&MotorConfig[i], &(eeprom->config), sizeof(TMotorConfig));  //Take data from EEPROM
    }
    else
    {
      memcpy(&(eeprom->config), &MotorConfig[i], sizeof(TMotorConfig));  //Put default data to EEPROM
    }
  }
}

