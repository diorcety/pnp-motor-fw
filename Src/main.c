/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */
  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */

  LL_APB2_GRP1_EnableClock(LL_APB2_GRP1_PERIPH_SYSCFG);
  LL_APB1_GRP1_EnableClock(LL_APB1_GRP1_PERIPH_PWR);

  /* System interrupt init*/

  /* USER CODE BEGIN Init */
  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */
  LL_SYSTICK_EnableIT();
  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_ADC_Init();
  MX_USART2_UART_Init();
  MX_SPI1_Init();
  MX_IWDG_Init();
  MX_CRC_Init();
  /* USER CODE BEGIN 2 */

  // Init hardware
  InitEeprom();
  LoadEeprom();
  InitADC();
  InitSPI();
  InitUART(ModuleConfig.SerialBitrate);

  //Wait for TMC5072 to startup
  LL_mDelay(20);

#if !defined(DEBUG)
  LL_IWDG_ReloadCounter(IWDG);
#endif

  //Init protocol
  InitMotorDrivers();
  InitTMCL();
  SetUARTTransmitDelay(3);
  ENABLE_DRIVERS();

#if !defined(DEBUG)
  LL_IWDG_ReloadCounter(IWDG);
#endif

  UINT WDGDelay = GetSysTimer();
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
    ProcessCommand();
    SystemControl();
    UpdateEeprom();

    if (abs(GetSysTimer() - WDGDelay) > 1000)
    {
      LL_GPIO_TogglePin(OUT1_GPIO_Port, OUT1_Pin);
      WDGDelay = GetSysTimer();
    }

#if !defined(DEBUG)
    LL_IWDG_ReloadCounter(IWDG);
#endif
  }
  /* USER CODE END 3 */
}

